    private void btnPlateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlateActionPerformed
        
        enablePlate(false);
        connectToSql();
        
        companyProcess = new CompanyProcess();
        
        try
        {
            companyProcess.setVehicles(txtPlate.getText(), txtPlate2.getText());
        }
        catch(Exception ex)
        {
            setStatus("Tartým hatasý: " + ex.toString());
            enablePlate(true);
            return;
        }
        
        txtWeight1.setText(companyProcess.emptyWeight.toString(false));
        txtVrkme1.setText(companyProcess.emptyWeight.getUOM());
        
        setStatus("Lütfen tartýmý gerçekleþtirin");
        btnWeigh1.setEnabled(true);
    }//GEN-LAST:event_btnPlateActionPerformed
    
