    private void btnWeigh1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWeigh1ActionPerformed
        btnWeigh1.setEnabled(false);
        
        if (companyProcess.getWeightStyle() == CompanyProcess.WEIGHT_STYLE.SINGLE)
        {
            if (Main.config.intParam.manualWeight)
            {
                try
                {
                companyProcess.emptyWeight = new Weight();
                companyProcess.emptyWeight.setWeight(txtWeight1.getText(), txtVrkme1.getText());                    
                }
                catch(Exception ex)
                {
                    Main.appendLog("Aðýrlýk hatasý: " + ex.toString());
                    setStatus("Lütfen aðýrlýklarý kontrol edin");
                    return;
                }
            }
            else
            {
                Main.steelYard.truckStatus = askan.steelyard.TwoWaySerialComm.TRUCK_STATUS.EMPTY;
                companyProcess.emptyWeight = Main.steelYard.getLastWeight();
            }
        }
        else
        {
            if (Main.config.intParam.manualWeight)
            {
                try
                {
                companyProcess.fullWeight = new Weight();
                companyProcess.fullWeight.setWeight(txtWeightTotal.getText(), txtVrkmeTotal.getText());
                }
                catch(Exception ex)
                {
                    Main.appendLog("Aðýrlýk hatasý: " + ex.toString());
                    setStatus("Lütfen aðýrlýklarý kontrol edin");
                    return;
                }                
            }
            else
            {
                Main.steelYard.truckStatus = askan.steelyard.TwoWaySerialComm.TRUCK_STATUS.FULL;
                companyProcess.fullWeight = Main.steelYard.getLastWeight();
            }
        }
        
        companyProcess.calculateCalcWeight();
        
        if (!Main.config.intParam.manualWeight)
        {
            txtWeight1.setText(companyProcess.emptyWeight.toString(false));
            txtVrkme1.setText(companyProcess.emptyWeight.getUOM());
            
            txtWeightTotal.setText(companyProcess.fullWeight.toString(false));
            txtVrkmeTotal.setText(companyProcess.fullWeight.getUOM());            
        }
        
        txtWeight2.setText(companyProcess.calcWeight.toString(false));
        txtVrkme2.setText(companyProcess.calcWeight.getUOM());      
        
        setStatus("Aðýrlýk hesaplandý, kaydedebilirsiniz");
        btnSave.setEnabled(true);
    }//GEN-LAST:event_btnWeigh1ActionPerformed

